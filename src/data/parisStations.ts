export const parisStations = stationsData.sort((a, b) => a.name.localeCompare(b.name, 'fr'));

export function searchStations(query: string): Station[] {
  if (!query || query.length < 1) return parisStations.slice(0, 20);
  
  const normalizedQuery = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
  
  return parisStations
    .filter(station => {
      const normalizedName = station.name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
      return normalizedName.includes(normalizedQuery);
    })
    .slice(0, 20);
}

export function getStationTypeLabel(type: 'metro' | 'rer' | 'both'): string {
  switch (type) {
    case 'metro': return 'Métro';
    case 'rer': return 'RER';
    case 'both': return 'Métro/RER';
  }
}

export function getStationTypeEmoji(type: 'metro' | 'rer' | 'both'): string {
  switch (type) {
    case 'metro': return '🚇';
    case 'rer': return '🚆';
    case 'both': return '🚇🚆';
  }
}
